#include <stdio.h>
 
int main()
{
    int a,b,c,d=0;
    scanf("%d %d",&a,&b);
    d=a/b;
    c=a%b+d;
    while(c>=b)
    {
        d+=c/b;
        c=c%b+c/b;
 	}
    printf("%d",d);
}

