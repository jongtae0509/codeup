#include<stdio.h>
int main()
{
	int n,i,j,k[1001];
	scanf("%d",&n);	
	for(i=0;i<n;i++)
	{
		scanf("%d",&k[i]);	
	}
	for(i=0;i<n;i++)
	{
		for(j=i;j<i+n;j++)
		{
			printf("%d ", k[j%n]);
		}
		printf("\n");
	}
	
}

