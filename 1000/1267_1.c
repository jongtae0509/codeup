#include<stdio.h>
int main(){
	int n,sum=0;
	int k[1001]={0};
	scanf("%d",&n);
	for(int i=0;i<n;i++){
		scanf("%d",&k[i]);
		if(k[i]%5==0){
		sum+=k[i];
		}
	}	
	printf("%d",sum);
}
