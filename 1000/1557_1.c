#include <stdio.h>

int n;




int f(int a){
	int c=0;
	for(int i=1;i<=a;i++){
    if (a%i==0){
        c++;
    	}
	}
	return c;
}
int main()
{
  scanf("%d", &n);
  printf("%d\n", f(n));
}
