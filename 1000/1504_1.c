#include <stdio.h>

int main(){
	int n;
	scanf("%d",&n);
	
	for(int i=0;i<n;i++){
		for(int j=0;j<n;j++){
			if(j%2==0){
				printf("%d ",j*n+i+1);
			}
			else{
				printf("%d ",j*n+n-i);
			}
		}
		puts("");
	}
}
