#include<stdio.h>
int main()
{
	int i,j,n,c,d,a[100][100]={};
	scanf("%d",&n);
	c=n;
	d=c;
	for(i=1;i<n+1;i++){
		for(j=0;j<n;j++){
			a[i-1][j]=c--;
			printf("%d ",a[i-1][j]);
		}
		c=d*(i+1);
		printf("\n");
	}
}
