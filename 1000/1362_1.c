#include<stdio.h>
int main(){
	int n,i,s,j,k;
	scanf("%d",&n);
	s=0;
	for(i=1;i<=n;i++){
		s+=i;
	}
	for(j=1;j<=n;j++){
		for(k=1;k<=j;k++){
			printf("%d ",s);
			s--;
		}
		printf("\n");
	}
}
