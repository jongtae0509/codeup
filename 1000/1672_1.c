#include<stdio.h>
int main(){
	int a,b;
	scanf("%d %d",&a,&b);
	int na;
	na=a/b;
	if(na>9999){
		printf("번호 초과 오류");
	}
	else{
	for(int i=1;i<=na;i++){
		printf("F-%04d\n",i);
	}
}
}
