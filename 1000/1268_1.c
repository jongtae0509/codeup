int main()
{
	int n,a[1001],sum=0;
	scanf("%d",&n);
	for(int i=0;i<n;i++){
		scanf("%d",&a[i]);
		if(a[i]%2==0){
			sum+=1;
		}
	}
	printf("%d",sum);
}
