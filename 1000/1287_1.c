#include<stdio.h>
int main(){
	int n,i,j;
	scanf("%d",&n);
	for(i=0;i<=9;i++){
		for(j=0;j<n*i;j++){
			printf("*");
		}
		if(i!=0){
		printf("\n");
		}
	}
}
