#include<stdio.h>

int main(){
	char a[21]={};
	scanf("%s",a);
	int c;
	int i;
	for(i=0;i<21;i++){
		if(a[i]==0){
			c=i;
			break;
		}
	}
	for(i=c-1;i>=0;i--){
		printf("%c",a[i]);
	}
}
