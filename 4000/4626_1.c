#include<stdio.h>
int main(){
	int n,i,s=0,sc=1;
	scanf("%d",&n);
	char a[n+1];
	for(i=0;i<n;i++){
		scanf("%s",&a[i]);
	}
	for(i=0;i<n;i++){
		if(a[i]=='1'){
			s+=sc;
		}
		if(a[i]=='1'&&a[i+1]=='1'){
			sc++;
		}
		if(a[i]=='0'){
			sc=1;
		}
	}
	printf("%d",s);
	
}
