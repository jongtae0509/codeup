#include<stdio.h>
int main(){
	int n,k,a[100000]={};
	scanf("%d",&n);
	for(int i=0;i<n;i++){
		scanf("%d",&a[i]);
	}
	scanf("%d",&k);
	for(int i=0;i<n;i++){
		if(a[i]==k){
			printf("%d",i+1);
			return 0;
		}
	}
	printf("-1");
}
